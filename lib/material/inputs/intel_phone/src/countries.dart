// see: https://en.wikipedia.org/wiki/List_of_country_calling_codes
// for list of country/calling codes

class PhoneFieldCountryEntity extends Equatable {
  final String name;
  final String arabicName;
  final String flag;
  final String code;
  final String dialCode;
  final String regionCode;
  final int minLength;
  final int maxLength;
  final RegExp? numberRegexValidation;

  const PhoneFieldCountryEntity({
    required this.name,
    required this.flag,
    required this.arabicName,
    required this.code,
    required this.dialCode,
    required this.minLength,
    required this.maxLength,
    this.regionCode = "",
    this.numberRegexValidation,
  });

  String get fullCountryCode {
    return dialCode + regionCode;
  }

  String get displayCC {
    if (regionCode != "") {
      return "$dialCode $regionCode";
    }
    return dialCode;
  }

  @override
  List<Object?> get props => [
        name,
        arabicName,
        flag,
        code,
        dialCode,
        regionCode,
        minLength,
        maxLength,
        numberRegexValidation,
      ];
}
